package work_lesson_1.homework_for_yesterday;

public class Figures {
    double weight;
    double length;
    String name;

    public Figures(double weight, double length, String name) {
        this.weight = weight;
        this.length = length;
        this.name = name;
    }

    public Figures(double length, String name) {
        this.length = length;
        this.name = name;
    }

    public Figures(String name) {
        this.name = name;
    }

    public String getName() {
        return name;
    }

    public String getArea() {
        return getArea();
    }
}
